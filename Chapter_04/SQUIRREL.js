function log_items(log) {
    let items = [];
    for (let item_list of log) {
        for (let item of item_list['events']) {
            if (!items.includes(item)) {
                items.push(item);
            }
        }
    }
    return items;
}


function correlation_values(item, log) {
    correlation_table = [0, 0, 0, 0]
    for (let item_list of log) {
        let index = 0;
        if (item_list['events'].includes(item)) index++;
        if (item_list['squirrel']) index += 2;
        correlation_table[index] += 1;
    }
    return correlation_table;
}

function phi(table) {
    return (table[3] * table[0] - table[2] * table[1]) /
        Math.sqrt((table[2] + table[3]) *
            (table[0] + table[1]) *
            (table[1] + table[3]) *
            (table[0] + table[2]));
}


